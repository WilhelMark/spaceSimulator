#include <iostream>
#include <cmath> 

int main() {
    // Объявление переменных
    float F; // Сила тяги в Ньютонах
    float m; // Масса космического корабля в килограммах
    float t; // Время работы двигателя в секундах

    // Запрос ввода данных у пользователя
    std::cout << "Введите силу тяги (F) в Н: ";
    std::cin >> F;

    std::cout << "Введите массу космического корабля (m) в кг: ";
    std::cin >> m;

    std::cout << "Введите время работы двигателя (t) в секундах: ";
    std::cin >> t;

    // Проверка корректности введенных данных
    if (m <= 0) {
        std::cout << "Ошибка: масса должна быть положительным числом." << std::endl;
        return 1; // Завершаем программу с кодом ошибки
    }
    
    if (t < 0) {
        std::cout << "Ошибка: время не может быть отрицательным." << std::endl;
        return 1; // Завершаем программу с кодом ошибки
    }

    // Расчет ускорения
    float a = F / m;

    // Расчет расстояния
    float distance = (a * std::pow(t, 2)) / 2;

    // Вывод результата
    std::cout << "Космический корабль окажется на расстоянии: " 
              << distance << " метров от первоначального положения." << std::endl;

    return 0; 
}
